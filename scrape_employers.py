def generate_employer_id(name, index):
    """Generate a unique employer ID based on name and index."""
    clean = re.sub(r'[^a-zA-ZäöüÄÖÜß]', '', name.lower())
    hash_part = hashlib.md5(clean.encode()).hexdigest()[:6].upper()
    return f"DE-{hash_part}-{index:04d}"

