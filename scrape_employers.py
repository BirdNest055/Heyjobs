def extract_contact_from_search(company_name, search_results):
    """Try to extract contact info from search results."""
    info = {}
    if not search_results:
        return info
    
    for result in search_results:
        snippet = result.get('snippet', '')
        # Try to extract phone
        phone_match = re.search(r'(?:Tel|Telefon|Phone)[:\s]*([\+\d\s\-\(\)]{8,20})', snippet)
        if phone_match:
            info['telefon'] = phone_match.group(1).strip()
        # Try to extract email
        email_match = re.search(r'[\w\.-]+@[\w\.-]+\.\w+', snippet)
        if email_match:
            info['email'] = email_match.group(0)
        # Try to extract address
        addr_match = re.search(r'(\d{5}\s+[A-ZÄÖÜ][a-zäöüß]+(?:\s[a-zäöüß]+)*,\s*[\w\s\-\.]+(?:straße|str\.|weg|platz|allee|ring|damm|gasse|park|ufer|stieg))', snippet)
        if addr_match:
            info['adresse'] = addr_match.group(0)
    
    return info

